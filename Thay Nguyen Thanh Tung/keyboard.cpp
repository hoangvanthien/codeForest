    for (int i = n; i > 0; --i) {
        if (dp[i]) {
            string w;
            for (int j = n - i; j < n; ++j) w.pb(s[j]);
            w.pb('#');
            for (int j = 0; j < i; ++j) w.pb(t[j]);
            vector<int> F = Z(w);
            for (int j = 0; j < i; ++j) {
                int s = j + i + 1;
                if (F[s] == i - j) dp[j] = 1;
            }
        }
    }
